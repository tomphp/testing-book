<Term anchor="continuous-deployment" term="Continuous Deployment">
  <p>
    A software engineering approach where changes to the software are deployed to production without manual
    intervention. When developers commit their changes, they are automatically tested and deployed to production if
    all checks succeed.
  </p>
</Term>

<Term anchor="green-field" term="Green Field">
  <p>
    A green field software project is a brand new project that is starting from scratch.
  </p>
</Term>
